public class Assignment_Operator {
    public static void main(String[] args) {
        //"=" is called the assignment operator as it is used to assign values to a variable
        int value1=10;//Assigning 10 to variable value1
        System.out.println("The First value is: "+value1);
        int value2=value1;//Assigning value of variable value1 to value2
        System.out.println("The Second value is: "+value2);
    }
}