public class Short_hand {
    public static void main(String[] args) {
        //There are few short hand operations that are used to perform tasks or operations more fast
        /*
        +=->a=a+x,
        -=->a=a-x,
        *=->a=a*x,
        /=->a=a/x,
        %=->a=a%x
        */
        int a=10;
        a+=5;//a=a+5
        System.out.println(a);
    }
}
