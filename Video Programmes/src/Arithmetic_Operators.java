public class Arithmetic_Operators {
    public static void main(String[] args) {
        //The operators responsible for performing arithmetic operations are called Arithmetic operators.
        //There are 5 arithmetic operators->+,-,*,/,%
        int a=10;
        int b=5;
        System.out.println(a+b);//Adds both the numbers
        System.out.println(a-b);//Subtracts b from a
        System.out.println(a*b);//Multiplies a and b
        System.out.println(a/b);//Divides a by b
        System.out.println(a%b);//Gives the remainder when a is divided by b
        //Operation of two integers will always result in integer output.


    }
}
